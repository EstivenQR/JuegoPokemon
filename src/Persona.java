
public class Persona {

    String Nombre;
    String Apellidos;
    int Cedula ;
    int Horas;

    public Persona() {
    }

    public Persona(String Nombre, String Apellidos, int Cedula, int Horas) {
        this.Nombre = Nombre;
        this.Apellidos = Apellidos;
        this.Cedula = Cedula;
        this.Horas = Horas;
    }



}
